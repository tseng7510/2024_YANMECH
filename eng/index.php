              'func': 'mute'
            });
            postMessageToPlayer(player, {
              'event': 'command',
              'func': 'playVideo'
            });
            break;
          case 'pause':
            postMessageToPlayer(player, {
              'event': 'command',
              'func': 'pauseVideo'
            });
